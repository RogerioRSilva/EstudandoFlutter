// Quais os tipos de variáveis que existem no Dart?
void main() {
  // Tipos de variáveis em Dart
  int idade = 30; // Inteiro
  double altura = 1.75; // Ponto flutuante
  String nome = 'João'; // Texto
  bool ativo = true; // Booleano

  print('Idade: $idade');
  print('Altura: $altura');
  print('Nome: $nome');
  print('Ativo: $ativo');
}