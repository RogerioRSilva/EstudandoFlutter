// Estrutura de repetição em Dart

void main() {
  // Estrutura de repetição for
  print('Contagem de 1 a 5 usando for:');
  for (int i = 1; i <= 5; i++) {
    print(i);
  }

  // Estrutura de repetição while
  print('Contagem de 1 a 5 usando while:');
  int j = 1;
  while (j <= 5) {
    print(j);
    j++;
  }

  // Estrutura de repetição do-while
  print('Contagem de 1 a 5 usando do-while:');
  int k = 1;
  do {
    print(k);
    k++;
  } while (k <= 5);
}