// Operadores Lógicos em Dart
void main() {
  // Declaração de variáveis booleanas
  bool a = true;
  bool b = false;

  // Operadores lógicos
  bool and = a && b; // E lógico
  bool or = a || b; // Ou lógico
  bool notA = !a; // Não lógico

  // Exibição dos resultados
  print('A: $a');
  print('B: $b');
  print('A AND B: $and');
  print('A OR B: $or');
  print('NOT A: $notA');
}